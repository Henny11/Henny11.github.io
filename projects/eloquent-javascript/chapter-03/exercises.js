////////////////////////////////////////////////////////////////////////////////

function min(num1, num2 ){
return num1 <= num2 ? num1: num2;
}


////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num) {
  if(num % 2 === 0){
    return true
  }
  else if(num % 2 !== 0){
    return false
  }

}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, charchter) {
var amountOfChars = [];
  for(var char = 0; char <= string.length - 1; char++){
    if(string[char] ===  charchter){
      amountOfChars.push(string[char])
    }
    
  }
  return amountOfChars.length;
}

